@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;50 per day")
def submit():
    body = request.get_json(silent=True)
    if not body or "text" not in body or "creator_id" not in body:
        return jsonify({"error": "Missing required fields: text, creator_id"}), 400

    text = body["text"].strip()
    creator_id = body["creator_id"].strip()

    if not text:
        return jsonify({"error": "text must not be empty"}), 400

    content_id = str(uuid.uuid4())
    timestamp = datetime.now(timezone.utc).isoformat()

    # Signal 1: LLM classifier
    llm_score = classify_with_llm(text)

    # Signal 2: Stylometric heuristics
    stylo_score = compute_stylometric_score(text)

    # Confidence: weighted average per planning.md (0.6 LLM + 0.4 stylo)
    confidence = round(0.6 * llm_score + 0.4 * stylo_score, 4)
    attribution, label_text = generate_label(confidence)

    short_text_warning = len(text.split()) < 80

    # Write to audit log
    log_decision({
        "content_id": content_id,
        "creator_id": creator_id,
        "timestamp": timestamp,
        "attribution": attribution,
        "confidence": confidence,
        "llm_score": llm_score,
        "stylo_score": stylo_score,
        "label_text": label_text,
        "status": "classified",
    })

    response = {
        "content_id": content_id,
        "attribution": attribution,
        "confidence": confidence,
        "label_text": label_text,
        "signals": {
            "llm_score": round(llm_score, 4),
            "stylo_score": round(stylo_score, 4),
        },
    }
    if short_text_warning:
        response["short_text_warning"] = True

    return jsonify(response), 200


@app.route("/log", methods=["GET"])
def log():
    limit = request.args.get("limit", 20, type=int)
    entries = get_log(limit)
    return jsonify({"entries": entries}), 200


@app.route("/status/<content_id>", methods=["GET"])
def status(content_id):
    entry = get_entry(content_id)
    if not entry:
        return jsonify({"error": "content_id not found"}), 404
    return jsonify(entry), 200


@app.route("/appeal", methods=["POST"])
def appeal():
    body = request.get_json(silent=True)
    if not body or "content_id" not in body or "creator_reasoning" not in body:
        return jsonify({"error": "Missing required fields: content_id, creator_reasoning"}), 400

    content_id = body["content_id"].strip()
    creator_reasoning = body["creator_reasoning"].strip()

    # Look up original decision
    entry = get_entry(content_id)
    if not entry:
        return jsonify({"error": "content_id not found"}), 404

    # Don't allow duplicate appeals
    if entry.get("appeal_id"):
        return jsonify({"error": "An appeal has already been filed for this content"}), 400

    appeal_id = str(uuid.uuid4())
    appealed_at = datetime.now(timezone.utc).isoformat()

    # Update audit log entry
    con = sqlite3.connect(DB_PATH)
    con.execute("""
        UPDATE audit_log
        SET status = 'under_review',
            appeal_id = ?,
            appeal_reason = ?,
            appealed_at = ?
        WHERE content_id = ?
    """, (appeal_id, creator_reasoning, appealed_at, content_id))
    con.commit()
    con.close()

    return jsonify({
        "appeal_id": appeal_id,
        "content_id": content_id,
        "status": "under_review",
        "message": "Your appeal has been logged and is under review.",
    }), 200


